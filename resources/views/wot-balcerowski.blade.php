                            <p>Do podstawowych zadań WOT należy: obrona we wsp&oacute;łdziałaniu z wojskami operacyjnymi, samodzielne działania niekonwencjonalne, przeciwdywersyjne i przeciwdesantowe, zabezpieczenie przyjęcia sojuszniczych sił, zarządzanie kryzysowe, działania informacyjne.</p>

                            <h3>3. Kampania dyskredytacyjna wobec WOT w latach 2016-2022</h3>

                            <p>Analiza pokazuje, że pomimo początkowych trudności, WOT zdołały się rozwinąć i stanowią obecnie znaczącą siłę w polskim systemie obronnym. Kampania dyskredytacyjna była motywowana czynnikami politycznymi i obiektywnymi problemami okresu formowania.</p>

                            <p><strong>Dyskredytacja kompetencyjna</strong> - środowiska niechętne WOT przekonywały, że tworzenie WOT jest nieefektywne z punktu widzenia obronności państwa.</p>

                            <p><strong>Dyskredytacja ideologiczna</strong> - szkalowanie Żołnierzy Wyklętych, kt&oacute;rzy zostali patronami WOT.</p>

